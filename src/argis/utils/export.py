from __future__ import annotations

import csv
import html as htmlmod
import json
import zipfile
from io import BytesIO, StringIO
from pathlib import Path

import httpx


def to_json(results: dict[str, dict]) -> str:
    return json.dumps(results, indent=2)


def to_csv(results: dict[str, dict]) -> str:
    buffer = StringIO()
    writer = csv.writer(buffer)
    writer.writerow(["platform", "status", "url", "title", "emails"])
    for name, info in sorted(results.items()):
        emails = ", ".join(info.get("emails", [])) if info.get("emails") else ""
        writer.writerow([
            name,
            info["status"],
            info["url"],
            info.get("title", ""),
            emails,
        ])
    return buffer.getvalue()


def to_markdown(results: dict[str, dict], username: str) -> str:
    lines = [
        f"# Argis scan results for `@{username}`",
        "",
        "| Platform | Status | URL |",
        "|---|---|---|",
    ]
    for name, info in sorted(results.items()):
        lines.append(f"| {name} | {info['status']} | {info['url']} |")
    return "\n".join(lines) + "\n"


