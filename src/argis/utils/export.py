async def send_webhook(
    url: str,
    results: dict[str, dict],
    username: str,
    webhook_type: str = "slack",
) -> bool:
    found = sum(1 for r in results.values() if r["status"] == "FOUND")
    total = len(results)

    if webhook_type == "slack":
        payload = {
            "text": f"*Argis scan complete — @{username}*\n> {found}/{total} platforms found",
            "attachments": [
                {
                    "color": "good" if found > 0 else "warning",
                    "fields": [
                        {"title": "Username", "value": username, "short": True},
                        {"title": "Profiles Found", "value": str(found), "short": True},
                        {"title": "Total Scanned", "value": str(total), "short": True},
                    ],
                }
            ],
        }
    elif webhook_type == "discord":
        color = 0x22C55E if found > 0 else 0xF97316
        payload = {
            "embeds": [
                {
                    "title": f"Argis scan — @{username}",
                    "color": color,
                    "fields": [
                        {"name": "Username", "value": username, "inline": True},
                        {"name": "Profiles Found", "value": str(found), "inline": True},
                        {"name": "Total Scanned", "value": str(total), "inline": True},
                    ],
                    "footer": {"text": "Argis OSINT Scanner"},
                }
            ]
        }
    else:
        payload = {
            "username": username,
            "found": found,
            "total": total,
            "results": {k: v["status"] for k, v in results.items()},
        }

    async with httpx.AsyncClient(timeout=10.0) as client:
        try:
            resp = await client.post(url, json=payload)
            return resp.is_success
        except httpx.RequestError:
            return False
